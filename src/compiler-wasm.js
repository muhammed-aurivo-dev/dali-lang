'use strict';

const { validateProgramSecurity } = require('./security-validator');
const { createProgramIR, hashIR } = require('./ir');
const path = require('path');

function escapeJsString(value) {
  return JSON.stringify(String(value || ''));
}

function compileToWasmModuleSkeleton(ast, options = {}) {
  validateProgramSecurity(ast, { mode: String(options.securityMode || 'strict').toLowerCase() });
  const ir = createProgramIR(ast, {
    sourceLabel: options.sourceLabel || '',
    securityMode: options.securityMode || 'strict',
    targetClass: 'wasm-hybrid',
    includeTimestamp: false
  });
  const irHash = hashIR(ir);
  const preset = Array.isArray(ast?.presets) && ast.presets[0] ? ast.presets[0] : null;
  const presetName = String(preset?.name || '');
  const sourceLabel = String(options.sourceLabel || '');
  const defaultFallbackModulePath = sourceLabel
    ? `./${path.basename(sourceLabel)}.generated.js`
    : './preset.generated.js';

  return [
    "'use strict';",
    '',
    '// Generated by dali-lang compiler (WASM hybrid target).',
    '// Runtime order: try WASM init -> fallback to JS WebAudio generated module.',
    `const __daliIR = ${JSON.stringify(ir, null, 2)};`,
    `const __DEFAULT_FALLBACK_MODULE = ${escapeJsString(defaultFallbackModulePath)};`,
    '',
    'function __tryRequire(modPath, requireFn) {',
    '  const req = typeof requireFn === "function" ? requireFn : (typeof require === "function" ? require : null);',
    '  if (!req) return null;',
    '  try {',
    '    return req(modPath);',
    '  } catch (_) {',
    '    return null;',
    '  }',
    '}',
    '',
    'async function __loadWasmBytes(opts) {',
    '  if (!opts || typeof opts !== "object") return null;',
    '  if (opts.wasmBytes && (opts.wasmBytes instanceof Uint8Array || opts.wasmBytes instanceof ArrayBuffer)) {',
    '    return opts.wasmBytes instanceof Uint8Array ? opts.wasmBytes : new Uint8Array(opts.wasmBytes);',
    '  }',
    '  if (typeof opts.fetchWasm === "function") {',
    '    const fetched = await opts.fetchWasm();',
    '    if (fetched instanceof Uint8Array) return fetched;',
    '    if (fetched instanceof ArrayBuffer) return new Uint8Array(fetched);',
    '  }',
    '  return null;',
    '}',
    '',
    'function __bytesToBase64(bytes) {',
    '  const b = bytes instanceof Uint8Array ? bytes : new Uint8Array(bytes || 0);',
    '  if (typeof Buffer !== "undefined" && typeof Buffer.from === "function") {',
    '    return Buffer.from(b).toString("base64");',
    '  }',
    '  let binary = "";',
    '  for (let i = 0; i < b.length; i += 1) binary += String.fromCharCode(b[i]);',
    '  if (typeof btoa === "function") return btoa(binary);',
    '  throw new Error("[DALI WASM] cannot encode wasm bytes to base64 in this runtime");',
    '}',
    '',
    'function __buildWasmWorkletCode(workletName, wasmBase64) {',
    '  return [',
    "    \"'use strict';\",",
    '    `const DALI_WASM_B64 = ${JSON.stringify(wasmBase64)};`,',
    '    "function __decodeBase64ToBytes(b64) {",',
    '    "  const raw = atob(String(b64 || \\"\\"));",',
    '    "  const out = new Uint8Array(raw.length);",',
    '    "  for (let i = 0; i < raw.length; i += 1) out[i] = raw.charCodeAt(i) & 0xff;",',
    '    "  return out;",',
    '    "}",',
    '    "function __clamp(v) {",',
    '    "  if (!Number.isFinite(v)) return 0;",',
    '    "  if (v > 1) return 1;",',
    '    "  if (v < -1) return -1;",',
    '    "  return v;",',
    '    "}",',
    '    "class DaliWasmBridgeProcessor extends AudioWorkletProcessor {",',
    '    "  constructor() {",',
    '    "    super();",',
    '    "    this.__kernel = null;",',
    '    "    this.__kernelBlock = null;",',
    '    "    this.__setParam = null;",',
    '    "    this.__inputPtr = null;",',
    '    "    this.__outputPtr = null;",',
    '    "    this.__memory = null;",',
    '    "    this.__free = null;",',
    '    "    try {",',
    '    "      const bytes = __decodeBase64ToBytes(DALI_WASM_B64);",',
    '    "      const mod = new WebAssembly.Module(bytes);",',
    '    "      const instance = new WebAssembly.Instance(mod, {});",',
    '    "      const ex = instance && instance.exports ? instance.exports : {};",',
    '    "      this.__memory = ex.memory;",',
    '    "      const malloc = ex.malloc;",',
    '    "      this.__free = ex.free;",',
    '    "      this.__memory = ex.memory;",',
    '    "      const malloc = ex.malloc;",',
    '    "      this.__free = ex.free;",',
    '    "      this.__kernel = (typeof ex.process_sample === \\"function\\") ? ex.process_sample : ((typeof ex.processSample === \\"function\\") ? ex.processSample : null);",',
    '    "      this.__kernelBlock = (typeof ex.process_block === \\"function\\") ? ex.process_block : ((typeof ex.processBlock === \\"function\\") ? ex.processBlock : null);",',
    '    "      this.__setParam = (typeof ex.set_param === \\"function\\") ? ex.set_param : ((typeof ex.setParam === \\"function\\") ? ex.setParam : null);",',
    '    "      this.__setParam = (typeof ex.set_param === \\"function\\") ? ex.set_param : ((typeof ex.setParam === \\"function\\") ? ex.setParam : null);",',
    '    "      const init = (typeof ex.init === \\"function\\") ? ex.init : ((typeof ex.initialize === \\"function\\") ? ex.initialize : null);",',
    '    "      if (init) { try { init(sampleRate, 128); } catch (_) {} }",',
    '    "      if (malloc && this.__memory) {",',
    '    "        this.__inputPtr = malloc(128 * 4);",',
    '    "        this.__outputPtr = malloc(128 * 4);",',
    '    "      }",',
    '    "      this.port.onmessage = (e) => {",',
    '    "        if (e.data && e.data.type === \'set_param\' && this.__setParam) {",',
    '    "          try { this.__setParam(e.data.param_id, e.data.value); } catch (_) {}",',
    '    "        }",',
    '    "      };",',
    '    "      if (malloc && this.__memory) {",',
    '    "        this.__inputPtr = malloc(128 * 4);",',
    '    "        this.__outputPtr = malloc(128 * 4);",',
    '    "      }",',
    '    "      this.port.onmessage = (e) => {",',
    '    "        if (e.data && e.data.type === \'set_param\' && this.__setParam) {",',
    '    "          try { this.__setParam(e.data.param_id, e.data.value); } catch (_) {}",',
    '    "        }",',
    '    "      };",',
    '    "    } catch (err) {",',
    '    "      this.__kernel = null;",',
    '    "      this.__kernelBlock = null;",',
    '    "      this.__setParam = null;",',
    '    "    }",',
    '    "  }",',
    '    "  process(inputs, outputs) {",',
    '    "    const input = (inputs && inputs[0]) || [];",',
    '    "    const output = (outputs && outputs[0]) || [];",',
    '    "    const channels = Math.min(input.length, output.length);",',
    '    "    for (let ch = 0; ch < channels; ch += 1) {",',
    '    "      const inCh = input[ch] || output[ch];",',
    '    "      const outCh = output[ch];",',
    '    "      const frames = Math.min(inCh.length, outCh.length);",',
    '    "      if (this.__kernelBlock && this.__memory && this.__inputPtr && this.__outputPtr) {",',
    '    "        const inputView = new Float32Array(this.__memory.buffer, this.__inputPtr, frames);",',
    '    "        const outputView = new Float32Array(this.__memory.buffer, this.__outputPtr, frames);",',
    '    "        for (let i = 0; i < frames; i += 1) inputView[i] = inCh[i];",',
    '    "        try { this.__kernelBlock(this.__inputPtr, this.__outputPtr, frames); } catch (_) {}",',
    '    "        for (let i = 0; i < frames; i += 1) outCh[i] = __clamp(outputView[i]);",',
    '    "        continue;",',
    '    "      }",',
    '    "      if (this.__kernel) {",',
    '    "        for (let i = 0; i < frames; i += 1) {",',
    '    "          let s = Number(inCh[i]) || 0;",',
    '    "          try { s = this.__kernel(s); } catch (_) {}",',
    '    "          outCh[i] = __clamp(s);",',
    '    "        }",',
    '    "      } else {",',
    '    "        for (let i = 0; i < frames; i += 1) outCh[i] = __clamp(inCh[i]);",',
    '    "      }",',
    '    "    }",',
    '    "    return true;",',
    '    "  }",',
    '    "}",',
    '    `registerProcessor(${JSON.stringify(workletName)}, DaliWasmBridgeProcessor);`',
    '  ].join("\\n");',
    '}',
    '',
    'async function __ensureWasmWorkletModule(audioContext, workletName, wasmBytes) {',
    '  if (!audioContext || !audioContext.audioWorklet || typeof audioContext.audioWorklet.addModule !== "function") {',
    '    throw new Error("[DALI WASM] AudioWorklet not available for wasm graph bridge");',
    '  }',
    '  const regKey = "__daliWasmBridgeRegistryV1";',
    '  if (!audioContext[regKey]) audioContext[regKey] = Object.create(null);',
    '  if (audioContext[regKey][workletName]) return;',
    '  const code = __buildWasmWorkletCode(workletName, __bytesToBase64(wasmBytes));',
  '  const blob = new Blob([code], { type: "application/javascript" });',
    '  const url = URL.createObjectURL(blob);',
    '  try {',
    '    await audioContext.audioWorklet.addModule(url);',
    '    audioContext[regKey][workletName] = true;',
    '  } finally {',
    '    URL.revokeObjectURL(url);',
    '  }',
    '}',
    '',
    'function __createDefaultWasmGraphBuilder(wasmBytes, opts, status) {',
    '  if (!wasmBytes || !wasmBytes.byteLength) return null;',
    '  const workletName = String((opts && opts.workletName) || `dali_wasm_bridge_${String(__daliIR.irHash || "").slice(0, 12) || "default"}`);',
    '  return async function buildWasmGraph(audioContext, inputNode, outputNode) {',
    '    if (!audioContext || !inputNode || !outputNode) {',
    '      throw new Error("[DALI WASM] audioContext, inputNode and outputNode are required");',
    '    }',
    '    await __ensureWasmWorkletModule(audioContext, workletName, wasmBytes);',
    '    const node = new AudioWorkletNode(audioContext, workletName, {',
    '      numberOfInputs: 1,',
    '      numberOfOutputs: 1,',
    '      outputChannelCount: [2],',
    '      channelCountMode: "max",',
    '      channelInterpretation: "speakers"',
    '    });',
    '    inputNode.connect(node);',
    '    node.connect(outputNode);',
    '    return {',
    '      presetName: String(__daliIR && __daliIR.presets && __daliIR.presets[0] && __daliIR.presets[0].name || ""),',
    '      backend: "wasm-audioworklet-bridge",',
    '      mode: "wasm",',
    '      status,',
    '      disconnect() {',
    '        try { inputNode.disconnect(node); } catch (_) {}',
    '        try { node.disconnect(outputNode); } catch (_) {}',
    '        try { node.disconnect(); } catch (_) {}',
    '      }',
    '    };',
    '  };',
    '}',
    '',
    'async function initWasmRuntime(opts = {}) {',
    "  const status = { ok: false, mode: 'none', reason: '', wasm: { linked: false }, fallback: { loaded: false, modulePath: '' } };",
    '  const preferWasm = opts.preferWasm !== false;',
    '  const requireWasm = opts.requireWasm === true;',
    '  const allowFallback = requireWasm ? false : (opts.allowFallback !== false);',
    '',
    '  if (preferWasm) {',
    '    if (typeof WebAssembly === "undefined") {',
    "      status.reason = 'WebAssembly not available';",
    '    } else {',
    '      try {',
    '        const wasmBytes = await __loadWasmBytes(opts);',
    '        if (wasmBytes && wasmBytes.byteLength > 0) {',
    '          const result = await WebAssembly.instantiate(wasmBytes, opts.importObject || {});',
    '          const __exports = result && result.instance && result.instance.exports ? result.instance.exports : (result && result.exports ? result.exports : {});',
    '          status.ok = true;',
    "          status.mode = 'wasm';",
    '          status.wasm.linked = true;',
    '          status.wasm.byteLength = wasmBytes.byteLength;',
    '          const hookGraphBuilder = (typeof opts.buildGraphFromWasm === "function")',
    '            ? opts.buildGraphFromWasm(__exports, status, { wasmBytes })',
    '            : null;',
    '          const defaultGraphBuilder = __createDefaultWasmGraphBuilder(wasmBytes, opts, status);',
    '          const selectedGraphBuilder = (typeof hookGraphBuilder === "function") ? hookGraphBuilder : defaultGraphBuilder;',
    '          return {',
    '            status,',
    '            mode: status.mode,',
    '            wasmInstance: result && (result.instance || result),',
    '            wasmBytes,',
    '            exports: __exports,',
    '            buildGraph: selectedGraphBuilder,',
    '            buildGraphSafe: selectedGraphBuilder ? (async (...args) => Promise.resolve(selectedGraphBuilder(...args))) : null,',
    '            dsp: {',
    '              processBlock: (typeof __exports.process_block === "function")',
    '                ? __exports.process_block',
    '                : (typeof __exports.processBlock === "function" ? __exports.processBlock : null)',
    '            }',
    '          };',
    '        }',
    "        status.reason = 'No WASM bytes provided';",
    '      } catch (err) {',
    "        status.reason = err && err.message ? err.message : 'WASM init failed';",
    '      }',
    '    }',
    '  } else {',
    "    status.reason = 'WASM preference disabled';",
    '  }',
    '',
    '  if (allowFallback) {',
    '    const fallbackModulePath = String(opts.fallbackModulePath || __DEFAULT_FALLBACK_MODULE);',
    '    const mod = __tryRequire(fallbackModulePath, opts.requireFn);',
    '    if (mod && typeof mod.buildGraph === "function") {',
    '      status.ok = true;',
    "      status.mode = 'js-fallback';",
    '      status.fallback.loaded = true;',
    '      status.fallback.modulePath = fallbackModulePath;',
    '      return {',
    '        status,',
    '        mode: status.mode,',
    '        buildGraph: mod.buildGraph,',
    '        buildGraphSafe: typeof mod.buildGraphSafe === "function" ? mod.buildGraphSafe : null,',
    '        module: mod',
    '      };',
    '    }',
    '  }',
    '',
    "  throw new Error(`[DALI WASM] init failed: ${status.reason || 'unknown'} (fallback unavailable)`);",
    '}',
    '',
    'module.exports = {',
    `  presetName: ${JSON.stringify(presetName)},`,
    "  target: 'wasm-hybrid',",
    `  sourceLabel: ${escapeJsString(sourceLabel)},`,
    '  defaultFallbackModulePath: __DEFAULT_FALLBACK_MODULE,',
    `  irHash: ${JSON.stringify(irHash)},`,
    '  ir: __daliIR,',
    '  initWasmRuntime',
    '};',
    ''
  ].join('\n');
}

module.exports = {
  compileToWasmModuleSkeleton
};
